package number_class;

public class CompareTo {

	public static void main(String[] args) {
		Integer i=5;
         System.out.println(i.compareTo(3));
         System.out.println(i.compareTo(5));
         System.out.println(i.compareTo(8));
         Float f=3.23f;
         System.out.println(f.compareTo(3.9f));
         System.out.println(f.compareTo(3.02f));
         System.out.println(f.compareTo(3.23f));
	}

}
